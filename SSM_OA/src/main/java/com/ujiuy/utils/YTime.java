package com.ujiuy.utils;

import java.util.Date;

public class YTime {
    private String dname;
    private Date et;
    private Date st;

    public String getDname() {

        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public Date getEt() {
        return et;
    }

    public void setEt(Date et) {
        this.et = et;
    }

    public Date getSt() {
        return st;
    }

    public void setSt(Date st) {
        this.st = st;
    }
}
